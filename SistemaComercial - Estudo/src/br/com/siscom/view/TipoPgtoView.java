private void inserirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inserirButtonActionPerformed
        br.com.siscom.bean.TipoPgto tp = new br.com.siscom.bean.TipoPgto();
        entityManager.persist(tp);
        list.add(tp);
        int row = list.size()-1;
        masterTable.setRowSelectionInterval(row, row);
        masterTable.scrollRectToVisible(masterTable.getCellRect(row, 0, true));
}//GEN-LAST:event_inserirButtonActionPerformed

private void excluirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirButtonActionPerformed
    int[] selected = masterTable.getSelectedRows();
        List<br.com.siscom.bean.TipoPgto> toRemove = new ArrayList<br.com.siscom.bean.TipoPgto>(selected.length);
        for (int idx=0; idx<selected.length; idx++) {
            br.com.siscom.bean.TipoPgto tp = list.get(masterTable.convertRowIndexToModel(selected[idx]));
            toRemove.add(tp);
            entityManager.remove(tp);
        }
        list.removeAll(toRemove);
}//GEN-LAST:event_excluirButtonActionPerformed

private void cancelarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelarButtonActionPerformed
        entityManager.getTransaction().rollback();
        entityManager.getTransaction().begin();
        java.util.Collection data = query.getResultList();
        for (Object entity : data) {
            entityManager.refresh(entity);
        }
        list.clear();
        list.addAll(data);
}//GEN-LAST:event_cancelarButtonActionPerformed

private void salvarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarButtonActionPerformed
        try {
            entityManager.getTransaction().commit();
            entityManager.getTransaction().begin();
        } catch (RollbackException rex) {
            rex.printStackTrace();
            entityManager.getTransaction().begin();
            List<br.com.siscom.bean.TipoPgto> merged = new ArrayList<br.com.siscom.bean.TipoPgto>(list.size());
            for (br.com.siscom.bean.TipoPgto tp : list) {
                merged.add(entityManager.merge(tp));
            }
            list.clear();
            list.addAll(merged);
        }

}//GEN-LAST:event_salvarButtonActionPerformed

